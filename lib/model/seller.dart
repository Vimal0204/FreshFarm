class Seller {
  final String firstName;
  final String lastName;
  final int id;
  final String email;
  final String mobile;
  final String address;
  final String city;
  final String state;
  final int pincode;

  Seller({
    required this.email,
    required this.firstName,
    required this.lastName,
    required this.id,
    required this.mobile,
    required this.address,
    required this.city,
    required this.state,
    required this.pincode,
  });
}
